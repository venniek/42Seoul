#include "libft.h"

char		*ft_strdup(const char *s)
{
	char	*dst;
	size_t	i;

	i = ft_strlen(s);
	if (!(dst = (char *)malloc(sizeof(char) * i + 1)))
		return (NULL);
	i = 0;
	while (s[i])
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
